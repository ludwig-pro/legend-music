/**
 * Adds common variations to base SF Symbol names
 */
export type SFSymbolWithFill = BaseSFSymbol | `${BaseSFSymbol}.fill`;

/**
 * Add more variations as needed
 */
export type SFSymbolWithVariations =
    | BaseSFSymbol
    | `${BaseSFSymbol}.fill`
    | `${BaseSFSymbol}.circle`
    | `${BaseSFSymbol}.circle.fill`
    | `${BaseSFSymbol}.square`
    | `${BaseSFSymbol}.square.fill`
    | `${BaseSFSymbol}.slash`
    | `${BaseSFSymbol}.slash.fill`
    | `${BaseSFSymbol}.badge.plus`
    | `${BaseSFSymbol}.badge.minus`
    | `${BaseSFSymbol}.badge.checkmark`
    | `${BaseSFSymbol}.badge.xmark`;

/**
 * SF Symbol type that accepts any string but provides autocompletion for common symbols
 */
export type SFSymbols = SFSymbolWithVariations;
